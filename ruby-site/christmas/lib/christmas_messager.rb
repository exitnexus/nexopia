class ChristmasMessager

	attr_accessor :batch_id, :messages, :plus_vouchers, :offset

	def initialize(batch_id, offset)
		self.batch_id = batch_id
		self.messages = []
		self.plus_vouchers = PlusVoucher.find(self.batch_id, :batchid)
		self.offset = offset       
	end

	def generate_messages
		self.plus_vouchers.length.times {|i|
			self.add_user()
		}
	end

	def add_user()
		begin
			user = User.find(:first, USERS[self.offset])
			message = ChristmasMessage.new
			message.batchid = self.batch_id
			message.userid = user.userid
			message.code = plus_vouchers[self.messages.length].id
			message.plus = user.plus?
			message.active = user.activetime(true) > Time.now.to_i - Constants::MONTH_IN_SECONDS
			self.messages << message
		rescue
			$log.info "Failed to add a Christmas plus code for user #{self.offset}.", :error
			$log.object $!, :error
			$log.object $!.backtrace, :error
		ensure
			self.offset += 1
		end
	end

	def send_messages
		self.generate_messages
		self.messages.each {|message|
			message.send_message
		}
	end

