function ampp_map_age($age)
{
	if (!$age || $age < 14) // invalid conditions, don't give anything.
		return "";
	else if ($age >= 14 && $age < 17)
		return "a=3;";
	else if ($age >= 17 && $age < 25)
		return "a=4;";
	else if ($age >= 25 && $age < 35)
		return "a=5;";
	else if ($age >= 35 && $age < 45)
		return "a=6;";
	else if ($age >= 45 && $age < 55)
		return "a=7;";
	else if ($age >= 55 && $age < 65)
		return "a=8;";
	else
		return "a=9;";
}

